from marshmallow import Schema, fields

class TicketNoteSchema(Schema):
    id = fields.Str(dump_only=True)
    ticketId = fields.Str(attribute='ticket_id')
    content = fields.Str(required=True)
    author = fields.Function(lambda obj: obj.author.full_name if obj.author else 'Unknown')
    authorId = fields.Str(attribute='author_id')
    imageUrl = fields.Str(attribute='image_url')
    isInternal = fields.Bool(attribute='is_internal')
    createdAt = fields.DateTime(attribute='created_at', format='iso')

class TicketSchema(Schema):
    id = fields.Str(dump_only=True)
    title = fields.Str(required=True)
    description = fields.Str(required=True)
    status = fields.Str()
    priority = fields.Str()
    category = fields.Str()
    submitterName = fields.Str(attribute='submitter_name')
    submitterEmail = fields.Str(attribute='submitter_email')
    submitterPhone = fields.Str(attribute='submitter_phone')
    submitterDepartment = fields.Str(attribute='submitter_department')
    imageUrl = fields.Str(attribute='image_url')
    assignedTo = fields.Function(lambda obj: obj.assigned_user.full_name if obj.assigned_user else None)
    assignedToId = fields.Str(attribute='assigned_to_id')
    collaborators = fields.List(fields.Function(lambda u: u.full_name))
    collaboratorIds = fields.Function(lambda obj: [u.id for u in obj.collaborators])
    slaDeadline = fields.DateTime(attribute='sla_deadline', format='iso')
    slaStatus = fields.Str(attribute='sla_status')
    resolutionSummary = fields.Str(attribute='resolution_summary')
    resolvedAt = fields.DateTime(attribute='resolved_at', format='iso')
    createdAt = fields.DateTime(attribute='created_at', format='iso')
    updatedAt = fields.DateTime(attribute='updated_at', format='iso')
    notes = fields.List(fields.Nested(TicketNoteSchema))
